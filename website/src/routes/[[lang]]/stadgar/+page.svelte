<h2>Stadgar för Ekolivs (Ronnebygatans ekologiska livs och boklåda, ekonomisk förening)</h2>

<p>
  Senast reviderade vid årsstämma 2 april 2017.
</p>

<h3>§ 1 Firma</h3>

<p>
  Föreningens firma är Ronnebygatans ekologiska livs och boklåda, Ekonomisk Förening.
</p>

<h3>§ 2 Ändamål</h3>

<p>Föreningen har till ändamål att främja medlemmarnas ekonomiska intresse som konsumenter, genom att gemensamt köpa in ekologiska och närproducerade livsmedel, underlätta rättvisehandel för medlemmar och boende i området Södra innerstaden samt därmed förenlig verksamhet.</p>

<h3>§ 3 Säte</h3>

<p>Styrelsen har sitt säte i Malmö kommun.</p>

<h3>§ 4 Insatser</h3>

<p>Insatsen är 200 kr. Insatsen erlägges kontant i butik eller på angivet konto inom tre veckor efter det att medlemskap beviljats.</p>

<h3>§ 5 Medlemsavgift</h3>

<p>Medlemsavgiften beslutas av föreningsstämman och får vara högst 100 kr.</p>

<h3>§ 6 Medlemskap</h3>

<p>Till medlemskap kan antas envar som kan ställa sig bakom föreningens ändamål och målsättning. Ansökan om medlemskap kan göras i butiken eller via e-mail.</p>

<h3>§ 7 Uteslutning</h3>

<p>Medlem som inte fullföljer sina åligganden gentemot föreningen eller på annat sätt skadar eller motarbetar föreningen får uteslutas av styrelsen.</p>

<h3>§ 8 Utträde</h3>

<p>Anmälan om utträde ur föreningen skall ske skriftligen till styrelsen. Utträde ur föreningen äger rum vid nästkommande styrelsemöte att skriftlig begäran inkommit.</p>

<h3>§ 9 Förlust av rösträtt</h3>

<p>Medlem som uteslutits eller sagt upp sig till utträde förlorar genast rätten att delta i överläggningar och beslut om föreningens angelägenheter.</p>

<h3>§ 10 Återbetalning av insatser</h3>

<p>Medlem som lämnat föreningen har rätt att på begäran återfå sin insats enligt 4:e kap i Lagen om ekonomiska föreningar.</p>

<h3>§ 11 Styrelse</h3>

<p>Styrelsen väljs av föreningsstämman. Styrelsen består av lägst 3 och högst 7 ordinarie ledamöter samt högst 3 suppleanter. Mandatperiod är två år för ordinarie ledamöter. Halva antalet ledamöter väljs årligen. Suppleanter väljs för tiden fram till nästkommande ordinarie stämma.</p>

<h3>§ 12 Revisorer</h3>

<p>Föreningsstämman utser 1 ordinarie revisor och högst 1 suppleant. Revisorer väljs för tiden fram till nästkommande ordinarie stämma.</p>

<h3>§ 13 Firmateckning</h3>

<p>Föreningens firma tecknas av styrelsen eller av den eller de som styrelsen utser. Firman tecknas var för sig.</p>

<h3>§ 14 Årsredovisning</h3>

<p>Styrelsen ska lämna årsredovisningen till revisorerna senast sex veckor före ordinarie föreningsstämma.</p>

<h3>§ 15 Räkenskapsår</h3>

<p>Föreningens räkenskapsår omfattar 1 januari till 31 december.</p>

<h3>§ 16 Stämmor</h3>

<p>Ordinarie föreningsstämma äger rum före april månads utgång.</p>

<p>Extra stämma skall hållas när:</p>

<ul>
  <li>styrelsen anser att det behövs,</li>
  <li>revisorerna begär det,</li>
  <li>en tiondel av de röstberättigade begär det.</li>
</ul>

<h3>§ 17 Kallelse till stämmor</h3>

<p>Styrelsen kallar till föreningsstämma. Kallelse till föreningsstämma ska innehålla en uppgift om de ärenden som ska förekomma. Kallelsen ska utfärdas tidigast sex veckor före och senast två veckor före föreningsstämman.</p>

<p>Kallelse till föreningsstämma och andra meddelanden sker genom brev med posten eller via e-post till samtliga medlemmar. Då kallelse gått ut till föreningsstämman ska styrelsen omedelbart underrätta revisorerna om detta.</p>

<h3>§ 18 Föreningsstämma</h3>

<p>På ordinarie föreningsstämma skall följande ärenden förekomma:</p>

<ul>
  <li>Val av ordförande.</li>
  <li>Val av protokollförare.</li>
  <li>Fastställande av röstlängd.</li>
  <li>Val av två protokolljusterare tillika rösträknare.</li>
  <li>Fastställande av dagordning.</li>
  <li>Fråga om stämman utlysts i stadgeenlig ordning.</li>
  <li>Styrelsens årsredovisning av det gångna räkenskapsåret.</li>
  <li>Revisorernas berättelse.</li>
  <li>Fastställande av resultat- och balansräkning.</li>
  <li>Beslut om disposition av föreningens vinst eller förlust.</li>
  <li>Beslut om ansvarsfrihet för styrelsen.</li>
  <li>Bestämmande av av ersättning till de medlemmar som deltar aktivt i driften av verksamheten.</li>
  <li>Fastställande av årsavgift.</li>
  <li>Fastställande av antal ledamöter i styrelsen.</li>
  <li>Val av styrelseledamöter och suppleanter.</li>
  <li>Val av revisor och suppleant.</li>
  <li>Val av valberedning.</li>
  <li>Ärenden som av styrelse eller medlem hänskjutits till stämman och som varit styrelsen tillhanda senast en månad innan stämman.</li>
  <li>Övriga ärenden.</li>
</ul>

<h3>§ 19 Rösträtt</h3>

<p>Varje medlem äger en röst.</p>

<h3>§ 20 Ärende på stämman</h3>

<p>Medlem som vill ha ärende behandlat på föreningsstämma skall skriftligen anmäla detta till styrelsen senast sex veckor innan stämman.</p>

<h3>§ 21 Stadgeändring</h3>

<p>Beträffande stadgeändring gäller vad som föreskrivs i 7 kap, §34 och §35 i Lagen om ekonomiska föreningar (SFS 2016:1336).</p>

<h3>§ 22 Överskott</h3>

<p>Uppkommet överskott skall, sedan i lag föreskriven avsättning till reservfonden skett, avsättas till dispositionsfond.</p>

<h3>§ 23 Upplösning</h3>

<p>Vid föreningens upplösning skall behållna tillgångar efter det att medlemmarna fått tillbaka sina insatser, delas lika mellan medlemmarna.</p>

<h3>§ 24 Övrigt</h3>

<p>I övrigt gäller reglerna i Lagen om ekonomiska föreningar, 1987:667.</p>
